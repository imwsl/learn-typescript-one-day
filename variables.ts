// variables.ts
// typescript的基本类型

// Bool
let isDone:boolean = false

// 数值
let age:number = 12

// 字符串
let myname:string = 'Joy'

// 数组
let list:number[] = [1, 2, 3]
let list1:Array<number> = [1, 2, 3]

// 对象
let dog:object = {}

// 空值，表示没有返回
function print_all(): void {}

// 任意值
let goods:any = {}
let good_name:any = "Fish Box"

// 类型推断
let card_num = 1989
let card_name = "Fish"

// 联合类型
let dogname:string|number
dogname = "Hello"
dogname = 23



